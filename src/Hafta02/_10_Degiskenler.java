package Hafta02;

public class _10_Degiskenler {
    public static void main(String[] args) {

        int toplam=0; // değişkeni tanımlarken ilk değer vermek mecburiyeti yok
        int sayac=0; // istersen ilk değeri tanımlarken verebilirsin

        System.out.println(sayac);

        sayac = sayac+2; // 2  + : sayısal toplamı yaptı
        System.out.println("sayac="+sayac); // sayac=2  +:birleştirme yaptı.

        System.out.println("toplam = " + toplam);
        System.out.println("sayac = " + sayac);
    }
}
