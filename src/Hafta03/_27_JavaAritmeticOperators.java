        a=5;        // a nın değeri 5

        a=a+6;       // a nın değerini 6 artırdım : 11
        a+=6;       // a nın artış değer 6 : a nın değerini 6 artırdım : 17

        a=a-5;      // a nın değerini 5 azalt
        a-=5;       // a nın değerini 5 azalt

        a=a*5;      // a yı 5 ile çarp
        a*=5;       // a yı 5 ile çarp

        a=a/5;      // a yı 5 e böler
